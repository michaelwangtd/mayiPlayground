def is_question(line):
    tar_list = ['?', '？','吗','呢', '何', '几', '怎', '呀', '谁', '啊', '孰', '岂',  '恩', \
                '焉', '乎',   '什', '么','啥','哪','多','如', '什么','为何', '为啥',  '何人', '何时', \
                '几时', '哪个', '哪儿', '哪里', '在么', '多大', '多少',  '如何', '怎么', '怎样', '怎的', \
                '谁的', '为什么', '哪一个', '哪边儿', '多少钱', '怎么样', '怎么着', '是不是']
    for tar in tar_list:
        if tar in line:
            return True
    return False
